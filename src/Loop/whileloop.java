package Loop;

public class whileloop {
    public static void main(String[] args) {

        int i=7;
        while (i<=70)
        {
            System.out.println(i);
            i=i+7;
        }
        System.out.println("***********************");

        int j=90;
        while(j>=9)
        {
            System.out.println(j);
            j=j-9;
        }


    }
}
