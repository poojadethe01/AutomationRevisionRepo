package Loop;

public class forLoop {
    public static void main(String[] args) {

        for(int i=9;i<=90;i=i+9)
        {
            System.out.println(i);
        }
        System.out.println("+++++++++++++++++++++++++");

        for(int i=80;i>=8; i=i-8)
        {
            System.out.println(i);
        }
    }
}
